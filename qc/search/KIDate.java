package qc.search;

import java.util.Date;

public class KIDate {
    String key;
    Date min,max;
    public String getKey() {
        return key;
    }
    public void setKey(String key) {
        this.key = key;
    }
    public Date getMin() {
        return min;
    }
    public void setMin(Date min) {
        this.min = min;
    }
    public Date getMax() {
        return max;
    }
    public void setMax(Date max) {
        this.max = max;
    }
    
}

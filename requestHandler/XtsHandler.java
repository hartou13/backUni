package requestHandler;

public class XtsHandler {
    Integer source, arrive, mvt;
    Xt[] xts;
    
    public Integer getSource() {
        return source;
    }
    public void setSource(Integer source) {
        this.source = source;
    }
    public Integer getArrive() {
        return arrive;
    }
    public void setArrive(Integer arrive) {
        this.arrive = arrive;
    }
    public Xt[] getXts() {
        return xts;
    }
    public void setXts(Xt[] xts) {
        this.xts = xts;
    }
    public Integer getMvt() {
        return mvt;
    }
    public void setMvt(Integer mvt) {
        this.mvt = mvt;
    }
        
}
